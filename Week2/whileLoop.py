#continue
#   when k becomes 3, the continue statement is encountered, and the loop skips the remaining code for that iteration, moving on to the next iteration
print ("")

k = 1
while k < 7: 
    k=k+1 
    if k==3: 
        continue   #loop skips the remaining code
    print(k)
    

